/**
 * audit-guide-targets.mjs — Guide Me highlight-target ledger + verifier.
 * ====================================================================
 * Builds the authoritative ledger of every highlight target used by the
 * Guide Me flows and cross-checks each against:
 *   1. A real DOM anchor in the app (data-copilot / data-assistant-id),
 *      including dynamic/ternary/array-built ids.
 *   2. Registration in the UI registry (seed + curated), since the
 *      request_ui_highlight tool rejects unregistered ids.
 *
 * Emits docs/guide-me-target-ledger.md and prints any broken targets.
 *
 *   node scripts/audit-guide-targets.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const APP = path.join(ROOT, 'app');
const GUIDE_FILES = [
  'app/components/copilot/guides.ts',
  'app/components/copilot/guides.generic.ts',
];

function walk(dir) {
  const out = [];
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, e.name);
    if (e.isDirectory()) out.push(...walk(full));
    else if (e.name.endsWith('.tsx') || e.name.endsWith('.ts')) out.push(full);
  }
  return out;
}

// ---- 1. Targets referenced by the guide flows --------------------------
const ID_IN_SELECTOR = /data-(?:copilot|assistant-id)="([a-z0-9-]+)"/g;
const referenced = new Map(); // id -> Set(guideId)
let currentGuide = '(unknown)';
for (const f of GUIDE_FILES) {
  const lines = fs.readFileSync(path.join(ROOT, f), 'utf8').split(/\r?\n/);
  for (const line of lines) {
    const g = line.match(/^\s*id:\s*'([a-z0-9-]+)',/);
    // top-level workflow ids have a sibling name: — track loosely via known list later
    if (g) currentGuide = g[1];
    for (const m of line.matchAll(ID_IN_SELECTOR)) {
      if (!referenced.has(m[1])) referenced.set(m[1], new Set());
      referenced.get(m[1]).add(currentGuide);
    }
  }
}

// ---- 2. Actual DOM anchors present in the app --------------------------
const anchors = new Set();
const anchorSource = new Map();
function addAnchor(id, file) {
  anchors.add(id);
  if (!anchorSource.has(id)) anchorSource.set(id, path.relative(ROOT, file).replace(/\\/g, '/'));
}
for (const file of walk(APP)) {
  const t = fs.readFileSync(file, 'utf8');
  // static string-literal selectors
  for (const m of t.matchAll(/data-(?:copilot|assistant-id)="([a-z0-9-]+)"/g)) addAnchor(m[1], file);
  // dynamic: ternary  ? 'a' : 'b'
  for (const m of t.matchAll(/\?\s*'([a-z0-9-]+)'\s*:\s*'([a-z0-9-]+)'/g)) { addAnchor(m[1], file); addAnchor(m[2], file); }
  // dynamic: cond ? 'a' : undefined
  for (const m of t.matchAll(/\?\s*'([a-z0-9-]+)'\s*:\s*undefined/g)) addAnchor(m[1], file);
  // array/object literal: copilot: 'x'  (nav items, resource cards)
  for (const m of t.matchAll(/copilot:\s*'([a-z0-9-]+)'/g)) addAnchor(m[1], file);
  // template-built: `resources-tab-${...}`  / `${copilotId}-add-btn` — record the prefix pattern
  for (const m of t.matchAll(/`([a-z0-9-]+)-\$\{/g)) addAnchor(m[1] + '-*', file);
  for (const m of t.matchAll(/\$\{[a-zA-Z0-9_.]+\}-([a-z0-9-]+)`/g)) addAnchor('*-' + m[1], file);
}

// ---- 3. Registry (seed + curated) --------------------------------------
const seedRaw = fs.readFileSync(path.join(APP, 'lib/assistant/uiRegistry.generated.ts'), 'utf8');
const registered = new Set();
for (const m of seedRaw.matchAll(/id:\s*"([a-z0-9-]+)"/g)) registered.add(m[1]);
const curatedRaw = fs.readFileSync(path.join(APP, 'lib/assistant/uiRegistry.ts'), 'utf8');
// curated keys: '<id>': {  — grab top-level CURATED entries
for (const m of curatedRaw.matchAll(/'([a-z0-9-]+)':\s*\{/g)) registered.add(m[1]);

// ---- 4. Reconcile -------------------------------------------------------
function anchorExists(id) {
  if (anchors.has(id)) return true;
  // template-pattern match (e.g. resources-tab-attachments matches resources-tab-*)
  for (const a of anchors) {
    if (a.endsWith('-*') && id.startsWith(a.slice(0, -1))) return true;
    if (a.startsWith('*-') && id.endsWith(a.slice(1))) return true;
  }
  return false;
}

const rows = [...referenced.keys()].sort().map((id) => ({
  id,
  flows: [...referenced.get(id)].join(', '),
  anchor: anchorExists(id),
  registered: registered.has(id),
  source: anchorSource.get(id) || '',
}));

const brokenAnchor = rows.filter((r) => !r.anchor);
const brokenReg = rows.filter((r) => r.anchor && !r.registered);

// ---- 5. Emit ledger -----------------------------------------------------
let md = `# Guide Me — Highlight Target Ledger\n\n`;
md += `> AUTO-GENERATED by \`scripts/audit-guide-targets.mjs\`. Do not edit by hand.\n`;
md += `> Re-run after adding/renaming any Guide Me highlight target or anchor.\n\n`;
md += `**Targets referenced by flows:** ${rows.length} · `;
md += `**Missing DOM anchor:** ${brokenAnchor.length} · `;
md += `**Anchor but unregistered:** ${brokenReg.length}\n\n`;
md += `| Target id | Flow(s) | DOM anchor | Registered | Source |\n`;
md += `|-----------|---------|:----------:|:----------:|--------|\n`;
for (const r of rows) {
  md += `| \`${r.id}\` | ${r.flows} | ${r.anchor ? '✅' : '❌'} | ${r.registered ? '✅' : '❌'} | ${r.source} |\n`;
}
fs.writeFileSync(path.join(ROOT, 'docs/guide-me-target-ledger.md'), md);

console.log(`Ledger written: docs/guide-me-target-ledger.md (${rows.length} targets)`);
console.log(`\n=== MISSING DOM ANCHOR (highlight will silently fail) ===`);
console.log(brokenAnchor.length ? brokenAnchor.map((r) => `  ${r.id}  [${r.flows}]`).join('\n') : '  (none)');
console.log(`\n=== HAS ANCHOR BUT NOT REGISTERED (request_ui_highlight rejects it) ===`);
console.log(brokenReg.length ? brokenReg.map((r) => `  ${r.id}  [${r.flows}]`).join('\n') : '  (none)');
