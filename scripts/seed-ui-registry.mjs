/**
 * seed-ui-registry.mjs — UI Element Registry seed (Phase 0B)
 * ==========================================================
 * Inventories existing `data-copilot="..."` anchors across the app and emits:
 *   1. app/lib/assistant/uiRegistry.generated.ts — a seed registry of every
 *      STATIC id found, with file location + inferred screenKey.
 *   2. A console migration checklist for DYNAMIC ids (data-copilot={...}) that
 *      must be registered by hand because their id is computed at runtime.
 *
 * This is a SEED, not the final registry. The curated `uiRegistry.ts` imports
 * the generated seed and layers labels/roles/descriptions on top. Re-run any
 * time as the app grows to catch newly-tagged elements.
 *
 *   node scripts/seed-ui-registry.mjs
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const APP = path.join(ROOT, 'app');
const OUT = path.join(ROOT, 'app', 'lib', 'assistant', 'uiRegistry.generated.ts');

function walk(dir) {
  const out = [];
  for (const e of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, e.name);
    if (e.isDirectory()) out.push(...walk(full));
    else if (e.name.endsWith('.tsx')) out.push(full);
  }
  return out;
}

/** Infer a coarse semantic screenKey from a file path. */
function inferScreen(relPath) {
  const p = relPath.replace(/\\/g, '/');
  const seg = p.match(/\[workspaceSlug\]\/([^/]+)/);
  if (seg) return seg[1];
  if (p.includes('/account/')) return 'account';
  if (p.includes('/components')) return 'components';
  return '';
}

const files = walk(APP);
const staticIds = new Map(); // id -> { file, screenKey }
const dynamic = []; // { file, line }

for (const file of files) {
  const rel = path.relative(ROOT, file);
  const text = fs.readFileSync(file, 'utf8');
  const lines = text.split(/\r?\n/);
  lines.forEach((line, i) => {
    for (const m of line.matchAll(/data-copilot="([^"]+)"/g)) {
      const id = m[1];
      if (!staticIds.has(id)) {
        staticIds.set(id, { file: rel, screenKey: inferScreen(rel) });
      }
    }
    if (/data-copilot=\{/.test(line)) {
      dynamic.push({ file: rel, line: line.trim() });
    }
  });
}

const sorted = [...staticIds.entries()].sort((a, b) => a[0].localeCompare(b[0]));

const header = `/**
 * AUTO-GENERATED by scripts/seed-ui-registry.mjs — DO NOT EDIT BY HAND.
 * Seed inventory of existing data-copilot anchors. The curated registry
 * (uiRegistry.ts) imports this and adds label/role/description.
 *
 * Static ids found: ${sorted.length}
 * Dynamic (hand-register) anchors: ${dynamic.length}
 */

export interface SeedEntry {
  /** Canonical assistant element id. */
  id: string;
  /** Coarse semantic screen key inferred from file path (curate as needed). */
  screenKey: string;
  /** Source file where the anchor was found (first occurrence). */
  source: string;
}

export const UI_REGISTRY_SEED: SeedEntry[] = [
`;

const body = sorted
  .map(
    ([id, info]) =>
      `  { id: ${JSON.stringify(id)}, screenKey: ${JSON.stringify(
        info.screenKey
      )}, source: ${JSON.stringify(info.file)} },`
  )
  .join('\n');

fs.writeFileSync(OUT, header + body + '\n];\n', 'utf8');

console.log(`Wrote ${path.relative(ROOT, OUT)} with ${sorted.length} static ids.`);
console.log('\nDYNAMIC anchors to register by hand:');
if (!dynamic.length) console.log('  (none)');
for (const d of dynamic) console.log(`  - ${d.file}: ${d.line}`);
